package no.ntnu.idi.freerider.protocol;

/** An enum indicating the status of a request made of the server. */
public enum ResponseStatus {
	/** The request was successfully fulfilled. */
	OK,
	/** The request could not be fulfilled. */
	FAILED,
	/** The status is not known. */
	UNKNOWN,
	/** Client error prevented the server from serving the request.*/
	CLIENT_ERROR;
}
